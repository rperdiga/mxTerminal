export type IconName = keyof typeof PATHS;

/** Returns an SVG string for the named Lucide icon at default 16x16. */
export function icon(name: IconName): string {
  return SVG_OPEN + PATHS[name] + SVG_CLOSE;
}

/** Mount an icon into an existing element by replacing its innerHTML. */
export function mountIcon(host: Element, name: IconName): void {
  host.innerHTML = icon(name);
}
